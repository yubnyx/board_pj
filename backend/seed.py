def run():
    db = SessionLocal()
    try:
        post_count = 0
        comment_count = 0
        for title, content, author, comments in SEED:
            post = models.Post(title=title, content=content, author=author)
            db.add(post)
            db.flush()  # post.id 확보
            post_count += 1
            for c_author, c_content in comments:
                db.add(
                    models.Comment(
                        post_id=post.id, author=c_author, content=c_content
                    )
                )
                comment_count += 1
        db.commit()
        print(f"✅ 글 {post_count}개, 댓글 {comment_count}개 추가 완료")
    finally:
        db.close()


if __name__ == "__main__":
    run()
